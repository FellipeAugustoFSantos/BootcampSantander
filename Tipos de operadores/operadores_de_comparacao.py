### Operadores de comparação são utilizados para realizar operações comparativas, e seu resultado sempre será um valor booleano, True ou False.

print(10 == 2)
print(10 == 10)
### Comparação de igualdade.

print(10 != 2)
print(10 != 10)
### Diferença

print(20 > 10)
print(10 > 20)
### Comparação maior que

print(10 >= 10)
print(10 >= 20)
print(10 >= 4)
### Comparação de maior ou igual

print(20 < 10)
print(10 < 20)
### Comparação menor que

print(10 <= 10)
print(10 <= 20)
print(10 <= 4)
### Comparação de menor ou igual