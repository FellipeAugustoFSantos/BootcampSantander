### Operadores de atribuição são utilizados para atribuir um valor inicial de uma variável ou sobrescrever o valor de uma variável.

valor = 500
print(valor)
### Atribuição simples

valor += 200
print(valor)
### Atribuição com adição

valor -= 200
print(valor)
### Atribuição com subtração

valor *= 2
print(valor)
### Atribuição com multiplicação

valor //= 2
print(valor)
### Atribuição com divisão inteira
### Curiosidade: se você realizar uma divisão inteira de um número float, o resultado dessa divisão será um número float.

valor /= 1
print(valor)
###Atribuição com divisão

valor %= 3
print(valor)
### Atribuição com módulo

valor **= 10
print(valor)
