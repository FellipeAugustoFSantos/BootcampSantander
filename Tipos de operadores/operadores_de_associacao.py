### Operadpres de associação são utilisados para verificar se um determinado objeto está presente na sequência.
numeros = [1, 2, 3, 4, 5, 6]
frase = "Estou adorando estudar Python!"

print("estudar" in frase)
print("Python" in frase)
print("amando" in frase)
### Verificando se determinada palavra se encontra na variável frase. Vale lembrar que letras maiúsculas fazem diferença na associação. 

print(6 not in numeros)
print(0 not in numeros)
